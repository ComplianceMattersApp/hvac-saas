import { readFile } from "node:fs/promises";
import path from "node:path";
import { notFound } from "next/navigation";

import { isProposalEmailPreviewViewerEnabled } from "@/lib/estimates/estimate-proposal-email-preview";

type PreviewMetadata = {
  generated_at?: string;
  recipient_email?: string;
  subject?: string;
};

async function readPreviewFile(filePath: string) {
  try {
    return await readFile(filePath, "utf8");
  } catch {
    return null;
  }
}

export default async function ProposalEmailPreviewPage() {
  if (!isProposalEmailPreviewViewerEnabled()) {
    notFound();
  }

  const outboxDir = path.join(process.cwd(), ".tmp", "email-outbox");
  const htmlPath = path.join(outboxDir, "latest-proposal-email.html");
  const jsonPath = path.join(outboxDir, "latest-proposal-email.json");

  const html = await readPreviewFile(htmlPath);
  const json = await readPreviewFile(jsonPath);

  let metadata: PreviewMetadata | null = null;
  if (json) {
    try {
      metadata = JSON.parse(json) as PreviewMetadata;
    } catch {
      metadata = null;
    }
  }

  return (
    <main className="mx-auto w-full max-w-6xl space-y-4 px-4 py-6 sm:px-6">
      <header className="space-y-1">
        <p className="text-[11px] font-semibold uppercase tracking-[0.12em] text-slate-500">
          Local Dev Only
        </p>
        <h1 className="text-xl font-semibold text-slate-950">Proposal Email Preview</h1>
        <p className="text-sm text-slate-600">
          Renders the latest proposal email generated by local preview mode.
        </p>
      </header>

      {metadata ? (
        <section className="rounded-xl border border-slate-200 bg-white p-4 text-sm text-slate-700">
          <p>
            <strong>To:</strong> {metadata.recipient_email || "-"}
          </p>
          <p>
            <strong>Subject:</strong> {metadata.subject || "-"}
          </p>
          <p>
            <strong>Generated:</strong> {metadata.generated_at || "-"}
          </p>
        </section>
      ) : null}

      {html ? (
        <section className="rounded-xl border border-slate-200 bg-white p-4">
          <div className="overflow-auto rounded-lg border border-slate-200 bg-white p-4">
            <div dangerouslySetInnerHTML={{ __html: html }} />
          </div>
        </section>
      ) : (
        <section className="rounded-xl border border-slate-200 bg-white p-4">
          <p className="text-sm text-slate-700">
            No proposal email preview has been generated yet.
          </p>
        </section>
      )}
    </main>
  );
}
